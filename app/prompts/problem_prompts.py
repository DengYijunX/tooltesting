def build_problem_prompt(topic: str, problem_background: str, retrieval_summary: str = "") -> str:
    return f"""你是“标准编程题题面草稿生成代理”。

你的任务是：围绕主题“{topic}”，根据给定背景和检索摘要，生成一道标准编程题的简洁草稿。

严格要求：
1. 题目的核心考点必须严格对应主题“{topic}”。
2. 必须是一道可判题的编程题，不能是问答题或开放题。
3. 输入输出必须明确、唯一、无歧义。
4. 不要输出 JSON。
5. 不要输出 Markdown 代码块。
6. 不要输出 LaTeX 公式写法，例如 \\(F = ma\\)，请直接写成普通文本：F = ma。
7. 只允许按下面固定格式输出，不要增加任何额外内容。
8. 如果资料不足以支持可靠出题，请输出：
题目名称：资料不足
任务描述：资料不足，无法可靠生成题目。

请严格按下面格式输出：

题目名称：...
题目描述：...
输入格式：...
输出格式：...
样例输入：...
样例输出：...
数据范围：...

背景如下：
{problem_background}

检索摘要如下：
{retrieval_summary}
"""